package org.yawlfoundation.yawl.editor.core.layout;

/**
 * An exception that may be thrown when parsing layout XML into layout objects
 *
 * @author Michael Adams
 * @date 7/06/12
 */
public class YLayoutParseException extends Exception {

    public YLayoutParseException() {
        super();
    }

    public YLayoutParseException(String s) {
        super(s);
    }

    public YLayoutParseException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public YLayoutParseException(Throwable throwable) { super(throwable); }
}
