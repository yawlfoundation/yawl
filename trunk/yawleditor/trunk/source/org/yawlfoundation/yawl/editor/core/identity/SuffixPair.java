package org.yawlfoundation.yawl.editor.core.identity;

/**
 * @author Michael Adams
 * @date 17/03/15
 */
public class SuffixPair {

    private int x;
    private int y;

    public SuffixPair(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() { return x; }

    public int getY() { return y; }

    public String former(String name) { return x == 0 ? name : name + "_" + x; }

    public String latter(String name) { return y == 0 ? name : name + "_" + y; }
}
